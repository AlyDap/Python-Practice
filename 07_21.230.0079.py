# Operasi Komparasi
a = 1
aa = 2
aaa = 1
print(f"{a} > {aa},{a>aa}")
print(f"{a} < {aa},{a<aa}")
print(f"{a} >= {aaa},{a>=aaa}")
print(f"{a} <= {aa},{a<=aa}")
print(f"{a} == {aaa},{a==aaa}")
print(f"{a} != {aa},{a!=aa}\n")

# Is Not
w = 2
e = 7
